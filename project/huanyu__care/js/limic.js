(function(){ 
var o = document.getElementById("limit_01"); 
var s = o.innerHTML; 
var p = document.createElement("div"); 
var n = document.createElement("a"); 
p.innerHTML = s.substring(0,100); 
n.innerHTML = s.length > 100 ? "更多" : ""; 
n.href = "###"; 
n.onclick = function(){ 
if (n.innerHTML == "更多"){ 
n.innerHTML = "收起"; 
p.innerHTML = s; 
}else{ 
n.innerHTML = "更多"; 
p.innerHTML = s.substring(0,100); 
} 
} 
o.innerHTML = ""; 
o.appendChild(p); 
o.appendChild(n); 
})(); 
